package stlab;

// src/Arithmetic.java
public class Arithmetic {

    // sum of two integers
    public int sum(int a, int b) {
        return a + b;
    }

    // sum of three integers (NEW METHOD)
    public int sum(int a, int b, int c) {
        return a + b + c;
    }
}