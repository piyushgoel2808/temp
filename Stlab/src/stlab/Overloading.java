package stlab;

// src/Overloading.java
public class Overloading {

    // add two integers
    public int add(int a, int b) {
        return a + b;
    }

    // add three integers
    public int add(int a, int b, int c) {
        return a + b + c;
    }

    // add two doubles
    public double add(double a, double b) {
        return a + b;
    }

    // concatenate two strings
    public String add(String a, String b) {
        return a + b;
    }
}