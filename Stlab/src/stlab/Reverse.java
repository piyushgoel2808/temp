package stlab;

// src/Reverse.java
public class Reverse {

    public String reverse(String input) {
        if (input == null) return null;

        char[] arr = input.toCharArray();
        int left = 0;
        int right = arr.length - 1;

        while (left < right) {
            char temp = arr[left];
            arr[left] = arr[right];
            arr[right] = temp;
            left++;
            right--;
        }

        return new String(arr);
    }
}